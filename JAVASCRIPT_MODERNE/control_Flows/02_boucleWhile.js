//Boucle [while]
let i=0;
while(i<5){
    console.log(i);
    i++
}

//Parcourir une liste des noms avec la boucle [while]
let noms = ['tayoro', 'dido', 'don', 'romaric'];
let j=0;
while(j < noms.length){
    console.log(j, noms[j])
    j++
}

