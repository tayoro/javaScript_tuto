//switch statement : La declaration de la conditionnelle avec [switch]
let mention = 18; 

switch(mention){
    case 'A':
        console.log('Vous avec reussi la mention A')
        break;
    case 'B':
        console.log('Vous avec reussi la mention B')
        break;
    case 'C':
        console.log('Vous avec reussi la mention C')
        break;
    case 'D':
        console.log('Vous avec reussi la mention D')
        break;
    default:
        console.log('cette mention n\'est pas disponible')
}